    // Helper methods
    public String getFullName() {
        String firstName = getFirstName();
        String lastName = getLastName();
        if (!firstName.isEmpty() && !lastName.isEmpty()) {
            return firstName + " " + lastName;
        } else if (!firstName.isEmpty()) {
            return firstName;
        } else if (!lastName.isEmpty()) {
            return lastName;
        } else {
            return getAliasName().isEmpty() ? getUsername() : getAliasName();
        }
    }

    public String getDisplayName() {
        String aliasName = getAliasName();
        if (!aliasName.isEmpty()) {
            return aliasName;
        }
        return getFullName();
    }

    public String getContactInfo() {
        StringBuilder contact = new StringBuilder();
        if (!getPhoneNumber().isEmpty()) {
            contact.append("Phone: ").append(getPhoneNumber());
        }
        if (!getEmailId().isEmpty()) {
            if (contact.length() > 0) contact.append("\n");
            contact.append("Email: ").append(getEmailId());
        }
        return contact.toString();
    }

    public String getAddressInfo() {
        StringBuilder address = new StringBuilder();
        if (!getOfficeAddress().isEmpty()) {
            address.append("Office: ").append(getOfficeAddress());
        }
        if (!getResidentialAddress().isEmpty()) {
            if (address.length() > 0) address.append("\n");
            address.append("Residential: ").append(getResidentialAddress());
        }
        return address.toString();
    }

    public String getBankInfo() {
        StringBuilder bankInfo = new StringBuilder();
        if (!getBankName().isEmpty()) {
            bankInfo.append("Bank: ").append(getBankName());
        }
        if (!getAccountNumber().isEmpty()) {
            if (bankInfo.length() > 0) bankInfo.append("\n");
            bankInfo.append("A/C: ").append(getAccountNumber());
        }
        if (!getIfscCode().isEmpty()) {
            if (bankInfo.length() > 0) bankInfo.append("\n");
            bankInfo.append("IFSC: ").append(getIfscCode());
        }
        return bankInfo.toString();
    }
